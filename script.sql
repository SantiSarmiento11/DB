DROP DATABASE IF EXISTS COLEGIO;
CREATE DATABASE COLEGIO;
USE COLEGIO;

CREATE TABLE ESTUDIANTES (
	ID INT AUTO_INCREMENT PRIMARY KEY,
	ID_MATRICULA INT NOT NULL,
	FISRT_NAME VARCHAR (30) NOT NULL,
	LAST_NAME VARCHAR (30) NOT NULL,
	EMAIL VARCHAR (30) NOT NULL
);


CREATE TABLE PROFESORES (
	ID INT AUTO_INCREMENT PRIMARY KEY,
	FN_PROFESOR VARCHAR (30) NOT NULL,
	LT_PROFESOR VARCHAR (30) NOT NULL,
	CUBICULO VARCHAR (30) NOT NULL
);

CREATE TABLE CURSOS (
	ID INT AUTO_INCREMENT PRIMARY KEY,
	CODIGO_CURSO VARCHAR (30) NOT NULL,
	NOMBRE_CURSO VARCHAR (30) NOT NULL,
	PROFESOR_ID INT NOT NULL,
	FOREIGN KEY (PROFESOR_ID) REFERENCES PROFESORES(ID)
);

CREATE TABLE NOTAS_PARCIALES (
	ID INT AUTO_INCREMENT PRIMARY KEY,
	ID_ESTUDIANTE INT NOT NULL,
	ID_CURSO INT NOT NULL,
	CALIFICACION DECIMAL (10,2) NOT NULL,
	FOREIGN KEY (ID_ESTUDIANTE) REFERENCES ESTUDIANTES(ID),
	FOREIGN KEY (ID_CURSO) REFERENCES CURSOS(ID)
);

CREATE TABLE MATRICULAS (
	ID INT AUTO_INCREMENT PRIMARY KEY,
	ID_ESTUDIANTE INT NOT NULL,
	ID_CURSO INT NOT NULL,
	FOREIGN KEY (ID_ESTUDIANTE) REFERENCES ESTUDIANTES(ID),
	FOREIGN KEY (ID_CURSO) REFERENCES CURSOS(ID)
);

